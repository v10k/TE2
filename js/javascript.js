botaoInserir = () => {
    const inserir = document.getElementById('inserir');
    const listar = document.getElementById('listar');
    inserir.style.display = 'flex';
    listar.style.display = 'none';
}

botaoListar = () => {
    const inserir = document.getElementById('inserir');
    const listar = document.getElementById('listar');
    listar.style.display = 'flex';
    inserir.style.display = 'none';
}